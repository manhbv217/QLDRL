package com.example.quanlidiemrenluyen.Models;

public class Point {

        int diemcong;
        int diemtru;

        public Point() {
        }

        public Point(int diemcong, int diemtru) {
            this.diemcong = diemcong;
            this.diemtru = diemtru;
        }

        public int getDiemcong() {
            return diemcong;
        }

        public void setDiemcong(int diemcong) {
            this.diemcong = diemcong;
        }

        public int getDiemtru() {
            return diemtru;
        }

        public void setDiemtru(int diemtru) {
            this.diemtru = diemtru;
        }
    }


